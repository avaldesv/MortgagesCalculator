/**
 * Writes Angular environment file from API_BASE_URL (Railway build).
 * Usage: API_BASE_URL=https://api.example.com node scripts/generate-environment.mjs production
 */
import { writeFileSync } from 'fs';
import { resolve } from 'path';

const mode = process.argv[2] || 'production';
const apiBaseUrl = (process.env.API_BASE_URL ?? '').replace(/\/$/, '');
const production = mode !== 'development';

const fileMap = {
  production: 'environment.ts',
  staging: 'environment.staging.ts',
  development: 'environment.development.ts',
};

const fileName = fileMap[mode] ?? fileMap.production;
const outPath = resolve(process.cwd(), 'src/environments', fileName);

const content = `/** Generated by scripts/generate-environment.mjs — do not edit in CI */
export const environment = {
  production: ${production},
  apiBaseUrl: '${apiBaseUrl.replace(/'/g, "\\'")}',
};
`;

writeFileSync(outPath, content, 'utf8');
console.log(`Wrote ${outPath} apiBaseUrl=${apiBaseUrl || '(empty)'}`);
